"""
Поиск релевантных книг по ключевым словам через TF-IDF
Цель: найти книги из локальной копии библиотеки liconism.com, которые
относятся к теме иконизма в восточных языках (японский, китайский, корейский).

Метод: TF-IDF (Term Frequency — Inverse Document Frequency).

Формат keywords.json:
  { "ключевое_слово": вес, ... }
  Веса используются как множители при суммировании TF-IDF компонент.
  Ключевые слова трактуются как префиксы — "ономатоп" найдёт
  "ономатопея", "ономатопоэтический" и т.д.

Зависимости:
    pip install pymupdf scikit-learn tqdm numpy
"""
import os
import json
import fitz  # PyMuPDF — извлечение текста из PDF
import numpy as np  # работа с матрицами и массивами чисел
from tqdm import tqdm
from sklearn.feature_extraction.text import TfidfVectorizer

# ══════════════════════════════════════════════════════════════════════════════
# КОНФИГУРАЦИЯ
# Все параметры сосредоточены здесь — менять только этот раздел.
# ══════════════════════════════════════════════════════════════════════════════

# Папка с PDF файлами (результат работы downloader.py)
pdf_folder = "downloads"

# Файл с ключевыми словами и их весами
keywords_file = "keywords.json"

# Минимальный нормированный TF-IDF балл для включения в результат.
# Диапазон: [0.0 .. 1.0], где 1.0 — самая релевантная книга в коллекции.
# Значение 0 отключает фильтрацию (вывести все документы).
cutoff = 0.01

# Ограничение на количество результатов в выводе (0 = без ограничений).
top_n = 0

# ══════════════════════════════════════════════════════════════════════════════
# ШАГ 1: ЗАГРУЗКА КЛЮЧЕВЫХ СЛОВ
# ══════════════════════════════════════════════════════════════════════════════

print("Загружаем ключевые слова из:", keywords_file)

# json.load возвращает dict[str, int|float] — словарь {термин: вес}
keywords: dict[str, float] = json.load(open(keywords_file))

print("Загружено ключевых слов:", len(keywords))

if cutoff > 0:
	print(f"Минимальный порог TF-IDF балла: {cutoff}")
else:
	print("Порог отсечения не задан")

# ══════════════════════════════════════════════════════════════════════════════
# ШАГ 2: ИЗВЛЕЧЕНИЕ ТЕКСТОВ ИЗ PDF
# ══════════════════════════════════════════════════════════════════════════════

# Собираем список всех PDF файлов в папке
pdf_files = [f for f in os.listdir(pdf_folder) if f.lower().endswith(".pdf")]
print(f"\nОбрабатываем {len(pdf_files)} PDF файлов в папке: {pdf_folder}")

# corpus — список строк, каждая строка = полный текст одной книги.
# Индексы corpus и valid_files всегда совпадают: corpus[i] ↔ valid_files[i].
corpus: list[str] = []
valid_files: list[str] = []

for filename in tqdm(pdf_files, desc="Чтение PDF"):
	file_path = os.path.join(pdf_folder, filename)
	text = ""
	try:
		# fitz.open открывает PDF; итерируемся постранично
		with fitz.open(file_path) as pdf:
			for page in pdf:
				# get_text() возвращает строку со всем текстом страницы.
				# .lower() — приводим к нижнему регистру для единообразия
				# (ключевые слова тоже будут в нижнем регистре при поиске)
				text += page.get_text().lower()

		corpus.append(text)
		valid_files.append(filename)

	except Exception as e:
		# Некоторые PDF могут быть повреждены или защищены паролем
		print(f"\nОшибка при чтении {filename}: {e}")

if not corpus:
	print("Не найдено ни одного читаемого PDF файла.")
	exit(1)

# ══════════════════════════════════════════════════════════════════════════════
# ШАГ 3: ПОСТРОЕНИЕ TF-IDF МАТРИЦЫ
# ══════════════════════════════════════════════════════════════════════════════

print(f"\nВычисляем TF-IDF по {len(corpus)} документам...")

# TfidfVectorizer принимает список текстов (corpus) и строит разреженную
# матрицу размером (N документов) × (M уникальных терминов).
# Каждая ячейка [i, j] = TF-IDF значение термина j в документе i.

vectorizer = TfidfVectorizer(
	# token_pattern — регулярное выражение, определяющее что считать словом.
	# Здесь: слова из кириллицы (а-яё) или латиницы (a-z), с дефисом, длина ≥ 2.
	# Это позволяет корректно обрабатывать смешанный русско-английский текст.
	token_pattern=r"[а-яёa-z][а-яёa-z\-]{1,}",

	# sublinear_tf=True заменяет TF на log(1 + TF).
	# Без этого книга с 200 упоминаниями слова получала бы в 10 раз больше очков,
	# чем книга с 20 упоминаниями. Логарифм сглаживает эту разницу.
	sublinear_tf=True,

	# min_df=1 — включать термин даже если он встречается только в 1 документе.
	# Нам важны редкие тематические термины (типа "ideophone").
	min_df=1,

	# max_df=0.95 — игнорировать термины, встречающиеся в >95% всех книг.
	# Такие слова (артикли, предлоги, общелингвистические термины) не несут
	# различительной информации.
	max_df=0.95,
)

# fit_transform делает два шага:
#   fit   — обходит весь corpus, строит словарь всех уникальных терминов
#           и вычисляет IDF для каждого термина
#   transform — кодирует каждый документ как вектор TF-IDF значений
# Результат: разреженная матрица scipy (экономит память — большинство ячеек = 0)
tfidf_matrix = vectorizer.fit_transform(corpus)

# Массив всех терминов, которые выучил векторизатор.
# feature_names[j] = термин, соответствующий j-й колонке tfidf_matrix.
feature_names = vectorizer.get_feature_names_out()

# Обратный индекс: термин → номер колонки. Нужен для быстрого поиска.
feature_index = {name: idx for idx, name in enumerate(feature_names)}

print(f"Словарь TF-IDF содержит {len(feature_names)} уникальных терминов.")

# ══════════════════════════════════════════════════════════════════════════════
# ШАГ 4: ВЫЧИСЛЕНИЕ ВЗВЕШЕННОГО СКОРА
# ══════════════════════════════════════════════════════════════════════════════

print("Вычисляем релевантность документов...")

# scores[i] — суммарный TF-IDF балл для i-го документа.
# Начинаем с нулей и накапливаем вклад каждого ключевого слова.
scores = np.zeros(len(corpus))

# Словарь для отладки: какие конкретные термины из TF-IDF словаря
# были найдены для каждого ключевого слова из keywords.json.
keyword_hits: dict[str, list[str]] = {}

for keyword, weight in keywords.items():
	kw = keyword.lower()

	# Ключевые слова в keywords.json — это корни/префиксы слов (ручной стемминг).
	# Например, "ономатоп" должен найти "ономатопея", "ономатопоэтический" и т.д.
	# Ищем все термины в TF-IDF словаре, которые начинаются с нашего ключевого слова
	# или содержат его как подстроку (для составных слов типа "звуко-цвет").
	matching_terms = [
		term for term in feature_names
		if term.startswith(kw) or kw in term
	]

	if matching_terms:
		keyword_hits[keyword] = matching_terms

		for term in matching_terms:
			# Извлекаем колонку матрицы для данного термина — вектор длиной N,
			# где N = количество документов. col[i] = TF-IDF термина в документе i.
			col_idx = feature_index[term]
			col = tfidf_matrix.getcol(col_idx).toarray().flatten()

			# Добавляем взвешенный вклад этого термина к общему скору.
			# Вес из keywords.json усиливает важные термины (вес 50)
			# и ослабляет второстепенные (вес 1).
			scores += col * weight

# Нормализация: приводим все скоры к диапазону [0, 1].
# Это делает результаты интерпретируемыми независимо от размера коллекции
# и количества ключевых слов. Книга с score=1.0 — самая релевантная.
max_score = scores.max()
if max_score > 0:
	normalized_scores = scores / max_score
else:
	normalized_scores = scores

# ══════════════════════════════════════════════════════════════════════════════
# ШАГ 5: ФИЛЬТРАЦИЯ И СОРТИРОВКА РЕЗУЛЬТАТОВ
# ══════════════════════════════════════════════════════════════════════════════

# Собираем результаты: только книги, прошедшие порог cutoff.
# Каждый элемент: (имя файла, нормированный скор, сырой скор).
results = [
	(filename, float(norm_score), float(raw_score))
	for filename, norm_score, raw_score in zip(valid_files, normalized_scores, scores)
	if cutoff == 0 or norm_score >= cutoff
]

# Сортируем по убыванию нормированного скора — самые релевантные первыми
results.sort(key=lambda x: x[1], reverse=True)

# Ограничиваем вывод top_n результатами (если задано)
if top_n > 0:
	results = results[:top_n]

# ══════════════════════════════════════════════════════════════════════════════
# ШАГ 6: ВЫВОД РЕЗУЛЬТАТОВ
# ══════════════════════════════════════════════════════════════════════════════

# Статистика по ключевым словам — полезно для отладки весов в keywords.json
print("\n── Статистика совпадений ключевых слов ─────────────────────────────")
for kw, terms in sorted(keyword_hits.items(), key=lambda x: -keywords[x[0]]):
	weight = keywords[kw]
	# Показываем максимум 5 найденных терминов для краткости
	print(f"  '{kw}' (вес {weight:>3}) → {len(terms)} терминов: {terms[:5]}")

print("\n══════════════════════════════════════════════════════════════════════")
if not results:
	print("Книг, соответствующих заданным параметрам, не найдено.")
	print(f"Попробуйте снизить порог cutoff (текущий: {cutoff})")
else:
	print(f"Найдено книг: {len(results)}  (порог cutoff ≥ {cutoff})")
	print("══════════════════════════════════════════════════════════════════════")
	for filename, norm_score, raw_score in results:
		# Визуальный индикатор релевантности: полоса из 20 символов █
		bar = "█" * int(norm_score * 20)
		print(f"{filename}")
		print(f"  [{bar:<20}] {norm_score:.4f}  (raw: {raw_score:.2f})")
	print("══════════════════════════════════════════════════════════════════════")

# ══════════════════════════════════════════════════════════════════════════════
# ДАННЫЕ ДЛЯ ДАЛЬНЕЙШЕЙ РАЗРАБОТКИ
#
# selected_texts — словарь, хранящий полные тексты всех книг,
# прошедших порог cutoff и вошедших в итоговый список results.
#
# Структура:
#   {
#       "filename.pdf": "полный текст книги в нижнем регистре...",
#       ...
#   }
#
# Индексация по имени файла (а не по числовому индексу) позволяет
# обращаться к тексту конкретной книги без привязки к порядку сортировки.
#
# Возможные применения в следующих итерациях проекта:
#   - дополнительная обработка NLP (именованные сущности, резюмирование)
#   - кластеризация отобранных книг по подтемам
#   - построение конкордансов (контекстный поиск вокруг ключевых слов)
#   - экспорт в базу данных или другой формат для анализа
#
# Примечание: тексты хранятся в памяти и не сохраняются на диск автоматически.
# ══════════════════════════════════════════════════════════════════════════════

# Множество имён файлов, прошедших отбор — для быстрой проверки принадлежности
# (проверка в множестве O(1) быстрее чем линейный поиск по списку results)
selected_filenames = {filename for filename, _, _ in results}

# Основной словарь: имя файла → полный текст книги.
# corpus и valid_files имеют одинаковые индексы, поэтому zip корректен.
selected_texts: dict[str, str] = {
	filename: text
	for filename, text in zip(valid_files, corpus)
	if filename in selected_filenames
}
